    private void ConfirmTransferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmTransferActionPerformed
        try {
            double transferAmount = Double.parseDouble(TransferAmount.getText());
            String sourceAccountNumber = AccountNumber.getText();
            String destinationAccountNumber = TransferNumber.getText();
            
            if (destinationAccountNumber.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please fill out the transfer account number");
                return;
            }
            
            if (sourceAccountNumber.equals(destinationAccountNumber)) {
                JOptionPane.showMessageDialog(null, "Cannot transfer to the same account");
                return;
            }
            
            if (transferAmount < 1) {
                JOptionPane.showMessageDialog(null, "Cannot transfer an amount below 1");
                return;
            }
            
            boolean sourceIsAccountOne = sourceAccountNumber.equals("123456789");
            boolean sourceIsAccountTwo = sourceAccountNumber.equals("987654321");
            boolean destinationIsAccountOne = destinationAccountNumber.equals("123456789");
            boolean destinationIsAccountTwo = destinationAccountNumber.equals("987654321");
            
            if (!(sourceIsAccountOne || sourceIsAccountTwo)) {
                JOptionPane.showMessageDialog(null, "Invalid source account number");
                return;
            }
            
            if (!(destinationIsAccountOne || destinationIsAccountTwo)) {
                JOptionPane.showMessageDialog(null, "Invalid destination account number");
                return;
            }
            
            double sourceAccountBalance = accounts.get(sourceAccountNumber);
            double destinationAccountBalance = accounts.get(destinationAccountNumber);
            
            if (transferAmount > sourceAccountBalance) {
                JOptionPane.showMessageDialog(null, "Cannot transfer because you do not have enough balance in your account");
                return;
            }
            
            sourceAccountBalance -= transferAmount;
            destinationAccountBalance += transferAmount;
            
            accounts.put(sourceAccountNumber, sourceAccountBalance);
            accounts.put(destinationAccountNumber, destinationAccountBalance);
            
            AccountNumberBalance.setText(Double.toString(sourceAccountBalance));
            
            JOptionPane.showMessageDialog(null, "Transfer successful with the amount of $" + transferAmount);
            JOptionPane.showMessageDialog(null, "New balance of your current account is $" + sourceAccountBalance);
            TransferAmount.setText("");
            TransferNumber.setText("");
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid input please enter a valid number.");
        }
    }//GEN-LAST:event_ConfirmTransferActionPerformed
    
