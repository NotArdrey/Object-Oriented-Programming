    private void EnterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnterButtonActionPerformed
        try {
            double withdrawAmount = Double.parseDouble(CalculatorTxtField.getText());
            
            if (withdrawAmount < 1) {
                JOptionPane.showMessageDialog(null, "Cannot withdraw an amount below 1");
            } else {
                String accountNumber = AccountNumber.getText();
                
                if (Menu.accounts.containsKey(accountNumber)) {
                    double currentBalance = Menu.accounts.get(accountNumber);
                    
                    if (withdrawAmount > currentBalance) {
                        JOptionPane.showMessageDialog(null, "Cannot withdraw because you do not have enough balance in your account");
                    } else {
                        double newBalance = currentBalance - withdrawAmount;
                        Menu.accounts.put(accountNumber, newBalance);
                        AccountNumberBalance.setText(Double.toString(newBalance));
                        
                        JOptionPane.showMessageDialog(null, "Withdrawal successful with the amount of $" + withdrawAmount);
                        JOptionPane.showMessageDialog(null, "New balance of your current account is $" + newBalance);
                        CalculatorTxtField.setText("");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Invalid account number please check the account number and try again.");
                }
            }
        } catch (NumberFormatException e) {
            System.out.println("Invalid input. Please enter a valid number.");
            CalculatorTxtField.setText("");
        }
        
    }//GEN-LAST:event_EnterButtonActionPerformed
    
